# Lista 01 - Exercício 07
# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

largura_quadrado = float(input("Digite a largura do quadrado:"))

area_quadrado = largura_quadrado * largura_quadrado

print("A área do quadrado é: {0} m²".format(area_quadrado))
print("O dobro da área é: {0} m²".format(area_quadrado * 2))